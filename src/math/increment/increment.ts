/**
 * Increments a given number by one.
 *
 * @param number - is a number to decrement by one
 * @return an incremented number by one
 *
 * @example
 * ```
 *  import { decrement } from "@commonly/Math"
 *
 *  decrement(7)    // -> 8
 * ```
 */
const increment = (number: number): number =>
    ++number



export default increment
